package src.basics_02;

public class Lab012 {
    public static void main(String[] args) {
        //Arithmetic operators

        double a=10; // = assignment operator:stores value from right to left
        double  b=34;
        double sum=a+b;
        double sub=a-b;
        double mul=a*b;
        double div=a/b;

        System.out.println(sum);
        System.out.println(sub); ///prints -ve sign
        System.out.println(mul);
        System.out.println(div); //0.29
    }
}
